package com.model.vo;

public class Attachment {
    private String attachmentId;

    private String attachmentName;

    private String attachmentSize;

    private String attachmentType;

    private String attachmentUploadTime;

    private String attachmentUploaderId;

    private String attachmentPath;

    private String attachmentRemark;

    private String attachmentFlag;

    private String attachmentUploaderName;

    private String attachmentControlId;

    private String attachmentRelationId;

    public String getAttachmentId() {
        return attachmentId;
    }

    public void setAttachmentId(String attachmentId) {
        this.attachmentId = attachmentId;
    }

    public String getAttachmentName() {
        return attachmentName;
    }

    public void setAttachmentName(String attachmentName) {
        this.attachmentName = attachmentName;
    }

    public String getAttachmentSize() {
        return attachmentSize;
    }

    public void setAttachmentSize(String attachmentSize) {
        this.attachmentSize = attachmentSize;
    }

    public String getAttachmentType() {
        return attachmentType;
    }

    public void setAttachmentType(String attachmentType) {
        this.attachmentType = attachmentType;
    }

    public String getAttachmentUploadTime() {
        return attachmentUploadTime;
    }

    public void setAttachmentUploadTime(String attachmentUploadTime) {
        this.attachmentUploadTime = attachmentUploadTime;
    }

    public String getAttachmentUploaderId() {
        return attachmentUploaderId;
    }

    public void setAttachmentUploaderId(String attachmentUploaderId) {
        this.attachmentUploaderId = attachmentUploaderId;
    }

    public String getAttachmentPath() {
        return attachmentPath;
    }

    public void setAttachmentPath(String attachmentPath) {
        this.attachmentPath = attachmentPath;
    }

    public String getAttachmentRemark() {
        return attachmentRemark;
    }

    public void setAttachmentRemark(String attachmentRemark) {
        this.attachmentRemark = attachmentRemark;
    }

    public String getAttachmentFlag() {
        return attachmentFlag;
    }

    public void setAttachmentFlag(String attachmentFlag) {
        this.attachmentFlag = attachmentFlag;
    }

    public String getAttachmentUploaderName() {
        return attachmentUploaderName;
    }

    public void setAttachmentUploaderName(String attachmentUploaderName) {
        this.attachmentUploaderName = attachmentUploaderName;
    }

    public String getAttachmentControlId() {
        return attachmentControlId;
    }

    public void setAttachmentControlId(String attachmentControlId) {
        this.attachmentControlId = attachmentControlId;
    }

    public String getAttachmentRelationId() {
        return attachmentRelationId;
    }

    public void setAttachmentRelationId(String attachmentRelationId) {
        this.attachmentRelationId = attachmentRelationId;
    }
}